declare(strict_types=1);

namespace League\Uri\Modifiers;

/**
 * Path component extension modifier
 *
 * @package    League\Uri
 * @subpackage League\Uri\Modifiers
 * @author     Ignace Nyamagana Butera <nyamsprod@gmail.com>
 * @since      1.0.0
 */
class Dirname implements UriMiddlewareInterface
{
    use PathMiddlewareTrait;
    use UriMiddlewareTrait;

    /**
     * The dirname to use for URI modification
     *
     * @var string
     */
    protected $dirname;

    /**
     * New instance
     *
     * @param string $dirname
     */
    public function __construct(string $dirname)
    {
        $this->dirname = (string) $this->filterSegment($dirname);
    }

    /**
     * Modify a URI part
     *
     * @param string $str the URI part string representation
     *
     * @return string the modified URI part string representation
     */
    protected function modifyPath(string $str): string
    {
        return (string) $this->filterSegment($str)->withDirname($this->dirname);
    }
}
